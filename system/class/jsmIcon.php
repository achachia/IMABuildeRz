    /**
     * jsmIcon::display()
     * 
     * @param string $type
     * @return
     */
    function display($type = 'fa')
    {
        $prefix = '';
        $title = '';
        $icon_list = array();
        switch ($type)
        {
            case 'ion':
                $title = 'Ionicons';
                $prefix = 'ion';
                $icon_list = $this->ionicon();
                break;
            case 'ion-md':
                $prefix = 'ion';
                $title = 'Ionicons';
                $icon_list = $this->ionicon();
                break;
            case 'fa':
                $prefix = 'fa';
                $title = 'Fontawesome';
                $icon_list = $this->fontAwesome();
                break;
            case 'dashicons':
                $prefix = 'dashicons';
                $title = 'Dashicons';
                $icon_list = $this->dashicons();
                break;
        }

        $html = null;
        $html .= '<div class="modal fade" id="' . $prefix . '-icon-dialog" role="dialog" tabindex="-1" aria-labelledby="modal-label-icon-dialog" aria-hidden="true">';
        $html .= '<div class="modal-dialog modal-lg">';
        $html .= '<div class="modal-content">';
        $html .= '<div class="modal-header">';
        $html .= '<button type="button" class="close" data-dismiss="modal" aria-hidden="true">&times;</button>';
        $html .= '<h4 class="modal-title" id="modal-label-icon-dialog">' . $title . '</h4>';
        $html .= '</div>';
        $html .= '<div class="modal-body">';
        $html .= '<div class="form-group">';
        $html .= '<input type="text" class="form-control" id="filter-' . $prefix . '-icon" placeholder="Search Icon"/>';
        $html .= '</div>';
        $html .= '<div class="icon-box">';
        if ($type !== 'ion')
        {
            foreach ($icon_list as $icon)
            {
                $html .= '<div class="col-xs-1 col-md-1 col-lg-1 ' . $prefix . '-icon-item icon-item" data-id="' . $icon['var'] . '">';
                $html .= '<a data-toggle="tooltip" data-placement="top" title="' . $icon['var'] . '" href="#' . $type . '-' . $icon['var'] . '" class="' . $prefix . '-icon-list icon-list" data-icon="' . $icon['var'] . '">';
                $html .= '<span class="icon ' . $type . ' ' . $type . '-' . $icon['var'] . ' ' . $type . '-md-' . $icon['var'] . ' ' . $type . '-ios-' . $icon['var'] . '"></span>';
                $html .= '</a>';
                $html .= '</div>';
            }
        } else
        {
            foreach ($icon_list as $icon)
            {
                $html .= '<div class="col-xs-1 col-md-1 col-lg-1 ' . $prefix . '-icon-item icon-item" data-id="' . $icon['var'] . '">';
                $html .= '<a data-toggle="tooltip" data-placement="top" title="' . $icon['var'] . '" href="#' . $type . '-' . $icon['var'] . '" class="' . $prefix . '-icon-list icon-list" data-icon="' . $icon['var'] . '">';
                $html .= '<ion-icon src="./templates/default/ionicons/svg/' . $icon['var'] . '.svg"></ion-icon>';
                // $html .= '<span class="icon ' . $type . ' ' . $type . '-' . $icon['var'] . ' ' . $type . '-md-' . $icon['var'] . ' ' . $type . '-ios-' . $icon['var'] . '"></span>';

                $html .= '</a>';
                $html .= '</div>';
            }
        }
        $html .= '</div>';
        $html .= '</div>';
        $html .= '<div class="modal-footer">';
        $html .= '<button type="button" id="modal-icon-dialog-button-dismiss"  class="btn btn-default btn-md" data-dismiss="modal">Close</button>';
        $html .= '</div>';
        $html .= '</div>';
        $html .= '</div>';
        $html .= '</div>';
        
        
        
        
        
        return $html;
    }
